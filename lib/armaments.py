RATINGS: list[int] = [
    S := 32,
    A := 16,
    B := 8,
    C := 4,
    D := 2,
    E := 1,
    _ := 0,
]


class ArmamentSpec:
    """
    Class representing an armament specification.
    """
    def __init__(self, name: str, type: str, affinities: list[str], STR: int, DEX: int, INT: int, FAI: int, ARC: int):
        self.name: str = name
        self.type: str = type
        self.affinities: list[str] = affinities
        self.STR: int = STR
        self.DEX: int = DEX
        self.INT: int = INT
        self.FAI: int = FAI
        self.ARC: int = ARC

    def get_best_stat(self) -> str:
        """
        Returns the name of the stat with the highest rating.
        """
        best_stat: int = max(self.STR, self.DEX, self.INT, self.FAI, self.ARC)
        res: list[str] = []
        if best_stat == self.STR:
            res.append("STR")
        if best_stat == self.DEX:
            res.append("DEX")
        if best_stat == self.INT:
            res.append("INT")
        if best_stat == self.FAI:
            res.append("FAI")
        if best_stat == self.ARC:
            res.append("ARC")
        return ", ".join(res)

    def get_stat_letter(self, stat_id: str) -> str:
        stat_value: int = getattr(self, stat_id)
        if stat_value == S:
            return "S"
        elif stat_value == A:
            return "A"
        elif stat_value == B:
            return "B"
        elif stat_value == C:
            return "C"
        elif stat_value == D:
            return "D"
        elif stat_value == E:
            return "E"
        else:
            return "-"

